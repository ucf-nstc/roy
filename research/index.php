      <h4><strong><em>Radiation Effects and Reliability of GaN/AlGaN High Electron Mobility Transistors</em></strong><br/><br />
      <strong>Electrical stress induced pinch-off voltage shifts </strong></h4>
      <p>We observed that hot electron stress on GaN/AlGaN HEMTs causes positive shift in Vpinch-off for GaN grown in Ga-rich and N-rich conditions by MBE, while it causes a negative shift in Vpinch-off for GaN grown in NH3-rich conditions  by MBE and by MOCVD. MOCVD is itself an NH3-rich process. Density functional theory (DFT) calculations confirmed that dehydrogenation of hydrogenated Ga-vacancies by energetic hot electrons causes the positive Vpinch-off shift, while dehydrogenation of hydrogenated N-antisites causes the negative Vpinch-off shift.</p>
      <p><em>T. Roy et al., Appl Phys. Lett. 96 (13), 133503 (2010)</em></p>
      <p><em>Y. S. Puzyrev et al., J. Appl. Phys. 109 (3), 034501 (2011)</em></p>
