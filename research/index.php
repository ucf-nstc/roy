      <h4><strong>Effect of electrical stress on low frequency 1/f noise</strong></h4>
      <p>What about 1/f noise in GaN HEMTs? After stress, some devices show an increase in noise while some devices surprisingly show a decrease in 1/f noise. DFT again shows that hydrogenated C in N-site gets dehydrogenated by hot electrons. The dehydrogenated C in N-site has a defect energy level far away from the Fermi level in the channel. Thus, this defect no longer contributes to noise after stress, thereby reducing the noise. N-antisite is again the culprit from increased noise after stress in some devices.</p>
      <p><em>T. Roy et al., Microelectron. Rel. 51 (2), 212-216 (2011)</em></p>
      <p><em>T. Roy et al., Intl. Rel. Phys. Symp. 4E. 4.1-4E. 4.4 (2011)</em></p>
      <img src="../static/images/research/10.png"/>
      <img src="../static/images/research/11.png"/>

      <h4><strong>Temperature dependence and microscopic origin of 1/f noise</strong></h4>
      <p>Temperature dependent 1/f noise spectroscopy is a beautiful tool to inform us about the nature of defects present in a system. We observe that Ga-rich, N-rich and NH3-rich devices all show a peak in 1/f noise spectra at 0.2 eV. However, N-rich devices show an additional peak at > 1 eV. Again DFT comes to the rescue and tells us that the peak at 0.2 eV is due to the reconfiguration of a O atom in N site, which is a metastable defect. The >1 eV peak is due to transition between negative and neutral state of a N antisite.</p>
      <p><em>T. Roy et al., Appl. Phys. Lett. 99 (20), 203501 (2011)</em></p>
      <img src="../static/images/research/12.png"/>
